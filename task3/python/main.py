import math  # Импорт математического модуля (хотя в данном коде не используется)

n = int(input("Введите количество чисел: "))  # Получаем количество чисел для обработки
for i in range(n):  # Цикл по всем числам (упрощен диапазон)
    num = int(input("Введите число: "))  # Читаем число 
    digSum = 0  # Инициализация суммы цифр
    digWork = 1  # Инициализация произведения цифр
    while num > 0:  # Разбираем число на цифры
        dig = num % 10  # Получаем последнюю цифру
        num = num // 10  # Удаляем последнюю цифру
        digSum += dig  # Суммируем цифры
        digWork *= dig  # Перемножаем цифры
    
    if digSum < digWork:  # Проверяем условие
        print(i)  # Выводим индекс числа (не само число!)