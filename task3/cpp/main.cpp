#include <iostream>
using namespace std;

int main() {
    int n;  // Количество чисел для обработки
    cout << "Введите количество чисел: ";
    cin >> n;
    for (int i = 0; i < n; i++) {
        int num;  // Текущее число
        cout << "Введите число: ";
        cin >> num;
        int digSum = 0, digWork = 1;  // Сумма и произведение цифр
        // Разбиваем число на цифры
        while (num > 0) {
            int dig = num % 10;  // Получаем последнюю цифру
            num /= 10;           // Убираем последнюю цифру из числа
            digSum += dig;       // Добавляем цифру к сумме
            digWork *= dig;     // Умножаем на произведение
        }
        // Проверяем условие и выводим индекс, если оно выполняется
        if (digSum < digWork) {
            cout << i << endl;  // Выводим индекс числа (не само число!)
        }
    }
    return 0;
}